# função para criaçao de matriz
def começo(m):
    matriz = [m*[0], m*[0], m*[0], m*[0]]
    for i in range(0, m):
        for j in range(0, m):
            matriz[i][j] = int(input(f"Digite o elemento [{i}, {j}]: "))
    return matriz


